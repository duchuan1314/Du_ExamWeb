package entity;

public class User {
   private String id;
   private String username;
   private String password;
   private String have_in;
   private int type_in;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getUsername() {
	return username;
}
public void setUsername(String username) {
	this.username = username;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public String getHave_in() {
	return have_in;
}
public void setHave_in(String have_in) {
	this.have_in = have_in;
}
public int getType_in() {
	return type_in;
}
public void setType_in(int type_in) {
	this.type_in = type_in;
}
   
}
